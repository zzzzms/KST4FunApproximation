function f = CubicSplines(t,t0,t1,t2,t3,t4)

f = ((t-t0).^3/((t3-t0)*(t2-t0)*(t1-t0))).*(t>=t0 & t<t1);
f = f+((t-t0).^2.*(t2-t)/((t3-t0)*(t2-t0)*(t2-t1))+(t-t0).*(t3-t).*(t-t1)/((t3-t0)*(t3-t1)*(t2-t1))+(t4-t).*(t-t1).^2/((t4-t1)*(t3-t1)*(t2-t1))).*(t>=t1 & t<t2);
f = f+((t4-t).^2.*(t-t2)/((t4-t1)*(t4-t2)*(t3-t2))+(t4-t).*(t-t1).*(t3-t)/((t4-t1)*(t3-t1)*(t3-t2))+(t-t0).*(t3-t).^2/((t3-t0)*(t3-t1)*(t3-t2))).*(t>=t2 & t<t3);
f = f + ((t4-t).^3/((t4-t1)*(t4-t2)*(t4-t3))).*(t>=t3 & t<t4);

end